export const populateVillages = async () => {
	const existingVillages = await queryMany<VillageRecord>(`SELECT * FROM villages`)
	const existingAdminPosts = await queryMany<AdminPostRecord>(`SELECT * FROM admin_posts`)
	await Promise.all(
		villages.map(async (village) => {
			const existingVillage = existingVillages.find((v) => v.code === village.code)
			if (existingVillage) {
				return
			}

			const existingAdminPost = existingAdminPosts.find((ap) => ap.code === village.admin_post_code)
			if (!existingAdminPost) {
				console.log('admin post not found', village.admin_post_code)
				console.log('Village', village)
				return
			}

			const newVillage = buildVillage({
				sync_id: 'GLOBAL',
				name: village.name,
				code: village.code,
				admin_post_id: existingAdminPost.id,
			})
			await insertVillage(newVillage)
		}),
	)
	console.log('villages count', villages.length)
}

