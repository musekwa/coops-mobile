import { CountryRecord, TABLES } from "src/library/powersync/schemas/AppSchema"
import { buildCountry } from "src/library/powersync/schemas/countries"
import { insertCountry, queryMany } from "src/library/powersync/sql-statements"


export const populateCountries = async () => {
    const countries = await queryMany<CountryRecord>(`SELECT * FROM ${TABLES.COUNTRIES}`, [])
    await Promise.all(
        countries_data.map(async (c) => {
            // Check if country already exists

            // Only insert if country doesn't exist
            if (!countries.find((country) => country.code === c.code)) {
                const country = buildCountry({
                    code: c.code,
                    name: c.name,
                    initials: c.initials,
                    sync_id: 'GLOBAL',
                })
                await insertCountry({ ...country })
            }
        }),
    )
}

