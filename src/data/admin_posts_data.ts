export const populateAdminPosts = async () => {
	const existingAdminPosts = await queryMany<AdminPostRecord>(`SELECT * FROM admin_posts`)
	const existingDistricts = await queryMany<DistrictRecord>(`SELECT * FROM districts`)

	await Promise.all(
		adminPosts.map(async (adminPost) => {
			const existingPost = existingAdminPosts.find((post) => post.code === adminPost.code)

			// update district code for some admin posts of sofala province
			if (adminPost.code.includes('21-07')) {
				await updateOne(
					`
					UPDATE ${TABLES.ADMIN_POSTS}
					SET district_id = (SELECT id FROM ${TABLES.DISTRICTS} WHERE code = ?)
					WHERE code = ?
					`,
					['21-07', adminPost.code],
				)
			}

			if (existingPost) {
				return
			}

			const existingDistrict = existingDistricts.find((district) => district.code === adminPost.district_code)

			if (!existingDistrict) {
				return
			}

			const post = buildAdminPost({
				sync_id: 'GLOBAL',
				name: adminPost.name,
				code: adminPost.code,
				district_id: existingDistrict.id,
			})
			await insertAdminPost(post)
		}),
	)
	console.log('admin posts count', adminPosts.length)
}

